def search_foods(query: str = "", category: str = "") -> list[dict]:
    results = FOODS
    if query:
        q = query.lower()
        results = [f for f in results if q in f["name"].lower() or q in f["category"].lower()]
    if category:
        results = [f for f in results if f["category"].lower() == category.lower()]
    return results


def get_food_by_id(food_id: str) -> dict | None:
    return next((f for f in FOODS if f["id"] == food_id), None)


